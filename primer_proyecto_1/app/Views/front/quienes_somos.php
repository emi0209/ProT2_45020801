<h1>    Quienes somos </h1>
<p> Sabores del Mundo nació del sueño compartido de sus fundadores, Valeria Gómez y Kenji Nakamura, dos apasionados de la cocina y la diversidad cultural. Inspirados por sus raíces y sus viajes, decidieron crear un espacio donde las recetas tradicionales de Asia, África y Europa pudieran convivir y llegar a todos los hogares del mundo.

La empresa fue fundada en 2020 con una visión clara: conectar culturas a través de la gastronomía, respetando la autenticidad de cada plato y celebrando la riqueza de la cocina global.

Hoy, Sabores del Mundo está conformada por un equipo multicultural de cocineros, investigadores gastronómicos, fotógrafos y desarrolladores de contenido, unidos por la misma pasión: contar historias a través de la comida.

Te invitamos a conocer a cada integrante del equipo a través de nuestras Cards, donde podrás ver sus rostros, especialidades y qué los inspira a ser parte de este proyecto. </p>

